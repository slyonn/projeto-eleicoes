    private void confirmarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_confirmarButtonActionPerformed
        if (numeroCandidatoText.getText() == null & presidenteText.getText().equals("Branco")){ 
            Urna.votarPresidenteBranco();
        } else {
            Urna.votarPresidente(numeroCandidatoText.getText());
        }
        confirmarButton.setEnabled(false);
        this.dispose();
        new TelaVotacaoGovernador(this).setVisible(true);
    }//GEN-LAST:event_confirmarButtonActionPerformed

