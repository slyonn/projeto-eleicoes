    public void pesquisarQuandoClicar(){
        String numeroPresidente = numeroCandidatoText.getText();
        if (CandidatosGerenciador.pesquisarPresidente(numeroPresidente) != null){
            presidenteText.setText(CandidatosGerenciador.pesquisarPresidente(numeroPresidente));
            System.out.println(presidenteText);
        } else {
            presidenteText.setText("Nulo");
            System.out.println(presidenteText);
        }
    }

